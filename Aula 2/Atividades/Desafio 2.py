# Faça um programa que leia um número inteiro e mostra na tela
# e seu sucessor e seu antecessor.

numeroInteiro = int(input("Digite um numero inteiro:"))
print(f"Numero digitado = {numeroInteiro}")

print("Numero anterior =", numeroInteiro- 1)
print("Numero sucessor =", numeroInteiro+ 1)

